SELECT REG.FISCAL_YEAR,
       REG.TERM_CODE TERM_CODE_IND,
       REG.STUDENT_PIDM STUDENT_PIDM_IND,
       REG.STUDENT_ID STUDENT_ID_IND,
       REG.MAU,
       REG.ACADEMIC_ORGANIZATION,
       REG.CAMPUS_CODE,
       REG.ACCREDITED_INSTITUTION,
       REG.COURSE_CRN,
       REG.SUBJECT_CODE,
       REG.COURSE_NUMBER,
       REG.SECTION_NUMBER,
       REG.CREDIT_ENR,
       REG.NON_CREDIT_ENR,
       REG.GRADE_MODE,
       REG.OFFICIAL_GRADE,
       REG.CREDIT_ATTEMPTED,
       REG.NONCREDIT_UNITS,
       TO_CHAR(REG.REGISTRATION_DATE, 'MM/DD/YYYY') AS REGISTRATION_DATE,
       REG.REGISTRATION_STATUS
  FROM DSDMGR.DSD_REGISTRATION REG
 WHERE REG.FISCAL_YEAR > 2010 AND REG.MAU = 'UAA'
